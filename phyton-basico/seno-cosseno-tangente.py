from math import sin, cos, tan, radians
angulo = float(input("Digite o 칙ngulo: "))
angulo = radians(angulo)
seno = sin(angulo)
cosseno = cos (angulo)
tangente = tan (angulo)
print(f"O 칙ngulo de {angulo} tem o SENO de {seno:.2f} ")
print(f"O 칙ngulo de {angulo} tem o COSSENO de {cosseno:.2f}")
print(f"O 칙ngulo de {angulo} tem a TANGENTE de {tangente:.2f}")
